#!/usr/bin/env python3

# Assignment short-hand notation, equivalent to
# x = 5
# y = 2

x, y = 5, 2

# Operators are similar to other language, except division

print(x * y)  # multiplication, 5 * 2 = 10
print(x ** y) # power(x, y) = 25

print(x / y)  # floating point division (2.5 expected)
print(x // y) # integer division (2 expected)
print(x % y)  # remainder (1 expected)

